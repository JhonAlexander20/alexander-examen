// Enumeración que representa los posibles resultados de un juego de Tic-Tac-Toe

enum class Winner {
    X, // Representa al jugador X como el ganador del juego
    O, // Representa al jugador O como el ganador del juego
    Draw // Representa un empate en el juego, donde ningún jugador es el ganador
}

